package com.hp.es.service.batchEntitlement;

/**
 * This class holds all the data for a SubRequest.
 */
public class SubRequest {

    /**
     * @return Returns the isoCountryCode.
     */
    public String getIsoCountryCode() {
        return isoCountryCode;
    }
    /**
     * @param isoCountryCode The isoCountryCode to set.
     */
    public void setIsoCountryCode(String isoCountryCode) {
        this.isoCountryCode = isoCountryCode;
    }
    /**
     * @return Returns the item.
     */
    public String getItem() {
        return item;
    }
    /**
     * @param item The item to set.
     */
    public void setItem(String item) {
        this.item = item;
    }
    /**
     * @return Returns the productId.
     */
    public String getProductId() {
        return productId;
    }
    /**
     * @param productId The productId to set.
     */
    public void setProductId(String productId) {
        this.productId = productId;
    }
    /**
     * @return Returns the response.
     */
    public String getResponse() {
        return response;
    }
    
    /**
     * @return Returns the csvResponse.
     */
    public String getCsvResponse() {
		return csvResponse;
	}
    
    /**
     * @param csvResponse The csvResponse to set.
     */
	public void setCsvResponse(String csvResponse) {
		this.csvResponse = csvResponse;
	}
	/**
     * @param response The response to set.
     */
    public void setResponse(String response) {
        this.response = response;
    }
    /**
     * @return Returns the requestId.
     */
    public String getRequestId() {
        return requestId;
    }
    /**
     * @param seq The seq to set.
     */
    public void setRequestId(String requestId) {
        this.requestId = requestId;
    }
    /**
     * @return Returns the serialNumber.
     */
    public String getSerialNumber() {
        return serialNumber;
    }
    /**
     * @param serialNumber The serialNumber to set.
     */
    public void setSerialNumber(String serialNumber) {
        this.serialNumber = serialNumber;
    }
    /**
     * @return Returns the status.
     */
    public String getStatus() {
        return status;
    }
    /**
     * @param status The status to set.
     */
    public void setStatus(String status) {
        this.status = status;
    }
    
    /**
     * @return Returns the includeContracts.
     */
    public boolean isIncludeContracts() {
        return includeContracts;
    }
    /**
     * @param includeContracts The includeContracts to set.
     */
    public void setIncludeContracts(boolean includeContracts) {
        this.includeContracts = includeContracts;
    }
    /**
     * @return Returns the includeOffers.
     */
    public boolean isIncludeOffers() {
        return includeOffers;
    }
    /**
     * @param includeOffers The includeOffers to set.
     */
    public void setIncludeOffers(boolean includeOffers) {
        this.includeOffers = includeOffers;
    }
    
    /**
     * @return Returns the check date
     */
    public String getCheckDate() {
		return checkDate;
	}
    /**
     * @param checkDate the check date to set
     */
	public void setCheckDate(String checkDate) {
		this.checkDate = checkDate;
	}

    /**
     * 
     */
    public String toString() {
        StringBuffer sb = new StringBuffer();
        sb.append("requestId=");
        sb.append(requestId);
        sb.append(" item=");
        sb.append(item);
        sb.append(" status=");
        sb.append(status);
        sb.append(" isoCountryCode=");
        sb.append(isoCountryCode);
        sb.append(" serialNumber=");
        sb.append(serialNumber);
        sb.append(" productId=");
        sb.append(productId);
        sb.append(" checkDate=");
        sb.append(checkDate);
        sb.append(" response=");
        sb.append(" csvResponse=");
        sb.append(response);
        sb.append(" includeOffers="+includeOffers);
        sb.append(" includeContracts="+includeContracts);
        return sb.toString();
    }
    
    private String requestId;
    private String item;
    private String status;
    private String isoCountryCode;
    private String serialNumber;
    private String productId;
    private String response;
    private String csvResponse;
    private String checkDate;
	private boolean includeOffers;
    private boolean includeContracts;
}

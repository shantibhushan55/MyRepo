package com.hp.es.service.util;
public interface EsLogLevel {

    /**
     * Logging includes <CODE>FUNCTIONAL</CODE> severity error level messages.
     */
    public static final int FUNCTIONAL_LOGGING = 2;

    /**
     * Logging includes <CODE>WARNING</CODE> severity error level messages.
     */
    public static final int WARNING_LOGGING = 3;

    /**
     * Logging includes <CODE>INFORMATION</CODE> severity error level messages.
     */
    public static final int INFORMATION_LOGGING = 4;

    /**
     * Logging includes verbose <CODE>DEBUG</CODE> severity error level messages.
     */
    public static final int DEBUG_LOGGING = 5;

    /**
     * Used for fatal exceptions and errors.
     */
    public static final int FATAL = 1;

    /**
     * Used for functional exceptions and errors.
     * @see EIAutil
     */
    public static final int FUNCTIONAL = 2;

    /**
     * Used for warning exceptions and errors.
     * @see EIAutil
     */
    public static final int WARNING = 3;

    /**
     * Used for informational messages.
     */
    public static final int INFORMATION = 4;

    /**
     * Used for verbose debug messages.

     */
    public static final int DEBUG = 5;
}

package com.hp.es.service.util;

/**
 * This interface is used as monitor for the current load on the service, i.e. how
 * many requests are currently running.
 */
public interface LoadChangeListener {

    /**
     * Is called whenever an operation is started.
     */
    void operationHasStarted();
    
    /**
     * is called whenever an operation has finished.
     */
    void operationHasFinished();
}
